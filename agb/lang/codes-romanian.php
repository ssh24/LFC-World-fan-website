              <LI>
                <P>Vechiul cod [URL] va functiona asa cum este detaliat mai jos.
				   Pentru aceasta doar incadrati link-ul intre paranteze
				   asa cum este prezentat in exemplul urmator (AGCode este
				   in <FONT color=#ff0000>rosu</FONT>).
				  <P>
                  <CENTER>
                    <FONT color=#ff0000>[url]</FONT>http://www.proxy2.de<FONT color=#ff0000>[/url]</FONT>
                  </CENTER>
                <P>In exemplu de mai sus, codul AGCode va genera automat
				   o legatura hiperlink la adresa URL. Deasemenea legatura
				   se va efectua intr-o fereastra separata in momentul in care
				   utilizatorul va face clik pe link. Retineti ca "http://"
				   parte integranta a adresei URL este optionala. In al doilea
				   exemplu de mai sus, legatura URL pe text va fi realizata dupa 
				   semnul egal. Deasemenea NU trebuie folosite ghilimele in
				   interiorul tag-ului URL. </P>
              </LI>
