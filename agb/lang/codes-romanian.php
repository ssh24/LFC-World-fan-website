              <LI><FONT color=#800000>www.yourURL.com </FONT>Puteti folosi
			  deasemenea adresa completa http:// sau varianta scurta cu
			  domeniul www. Daca site-ul nu incepe cu "www", va trebui
			  totusi adresa completa "http://". Deasemenea, puteti folosi
			  prefixe de legaturi http si ftp in modul auto-link
			  (cand codul AGCode este activat). <BR>
              <BR>
              <LI>Deasemenea puteti avea adevarate hyperlink-uri folosind codul [url]. 
